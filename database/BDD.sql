DROP TABLE IF EXISTS BookCategory, Reviews, LoanHistory, WrittenBy, Categories, Users, Copies, States, Authors, Books;

CREATE TABLE Books
(
    id_book               INT AUTO_INCREMENT,
    image_book            TEXT,
    publication_date_book DATE,
    name_book             VARCHAR(255),
    description_book      TEXT,
    PRIMARY KEY (id_book)
);

CREATE TABLE Authors
(
    id_author   INT AUTO_INCREMENT,
    name_author VARCHAR(255),
    PRIMARY KEY (id_author)
);

CREATE TABLE States
(
    id_state   INT,
    name_state VARCHAR(32),
    PRIMARY KEY (id_state)
);

CREATE TABLE Copies
(
    id_copy     INT AUTO_INCREMENT,
    isAvailable BOOLEAN,
    id_book     INT NOT NULL,
    id_state    INT NOT NULL,
    PRIMARY KEY (id_copy),
    FOREIGN KEY (id_book) REFERENCES Books (id_book),
    FOREIGN KEY (id_state) REFERENCES States (id_state)
);

CREATE TABLE Users
(
    id_user         INT AUTO_INCREMENT,
    username        VARCHAR(255),
    password        TEXT,
    first_name_user VARCHAR(255),
    last_name_user  VARCHAR(255),
    isAdmin         BOOLEAN,
    PRIMARY KEY (id_user)
);

CREATE TABLE Categories
(
    id_category   INT,
    name_category VARCHAR(32),
    PRIMARY KEY (id_category)
);

CREATE TABLE WrittenBy
(
    id_book   INT,
    id_author INT,
    PRIMARY KEY (id_book, id_author),
    FOREIGN KEY (id_book) REFERENCES Books (id_book),
    FOREIGN KEY (id_author) REFERENCES Authors (id_author)
);

CREATE TABLE LoanHistory
(
    id_loanhistory INT AUTO_INCREMENT,
    id_copy        INT,
    id_user        INT,
    start_loan     DATE,
    end_loan       DATE,
    PRIMARY KEY (id_loanhistory),
    FOREIGN KEY (id_copy) REFERENCES Copies (id_copy),
    FOREIGN KEY (id_user) REFERENCES Users (id_user)
);

CREATE TABLE Reviews
(
    id_book        INT,
    id_user        INT,
    content_review TEXT,
    date_review    DATE,
    PRIMARY KEY (id_book, id_user),
    FOREIGN KEY (id_book) REFERENCES Books (id_book),
    FOREIGN KEY (id_user) REFERENCES Users (id_user)
);

CREATE TABLE BookCategory
(
    id_book     INT,
    id_category INT,
    PRIMARY KEY (id_book, id_category),
    FOREIGN KEY (id_book) REFERENCES Books (id_book),
    FOREIGN KEY (id_category) REFERENCES Categories (id_category)
);

